package com.evote.model;

public class Calon {
	private String id_calon;
	private String no_urut;
	private String nama;
	private String angkatan;
	private String nim;
	public String getId_calon() {
		return id_calon;
	}
	public void setId_calon(String id_calon) {
		this.id_calon = id_calon;
	}
	public String getNo_urut() {
		return no_urut;
	}
	public void setNo_urut(String no_urut) {
		this.no_urut = no_urut;
	}
	public String getNama() {
		return nama;
	}
	public void setNama(String nama) {
		this.nama = nama;
	}
	public String getPhoto() {
		return photo;
	}
	public void setPhoto(String photo) {
		this.photo = photo;
	}
	public String getVisi() {
		return visi;
	}
	public void setVisi(String visi) {
		this.visi = visi;
	}
	public String getMisi() {
		return misi;
	}
	public void setMisi(String misi) {
		this.misi = misi;
	}
	public String getHasil() {
		return hasil;
	}
	public void setHasil(String hasil) {
		this.hasil = hasil;
	}
	public String getAngkatan() {
		return angkatan;
	}
	public void setAngkatan(String angkatan) {
		this.angkatan = angkatan;
	}
	public String getNim() {
		return nim;
	}
	public void setNim(String nim) {
		this.nim = nim;
	}
	private String photo;
	private String visi;
	private String misi;
	private String hasil;
	
}
